<?php
	//operadores aritméticos

	$a = 10;
	$b = 2;

		echo $a + $b . "<br>"; //soma dois dois numeros

		echo $a - $b . "<br>"; //result. subtração dos nº

		echo $a * $b . "<br>"; // result. multi. dos n°

		echo $a / $b . "<br>"; // result. divisão dos n°

		echo $a % $b . "<br>"; // result. do que sobra na   //divisão ou módulo exemplo: 10/2 = 0

		echo $a ** $b; // result. do $a ao quadrado de $b. // exemplo: 10²

?>